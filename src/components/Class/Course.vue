    <p>4.為控制學生的畢業程度，從97年度入學新生起，增加語言畢業門檻，必須通過俄語一級檢定方能畢業，因此加開一個學分的選修課俄語檢定，協助訓練同學們通過考試。</p>
    <p>5.課程的直向連結：大一俄語~大四俄語，大一會話~大四會話，商用俄文（一）、（二），筆譯（大三）、口譯（大四），「俄語短篇名著選讀」、「俄語小說與電影賞析」，俄語語法（一）、（二），實習（一）、（二），俄語視聽練習（一）、（二），將語言課程從基礎到應用，由淺入深，循序漸進的培養學生的俄語聽說讀寫譯五項技能。 </p>
    <p>6.課程的橫向連結：低年級開設文化課程誘發學生對俄文學習的興趣；高年級的實用課程則是鍛鍊學生的語言應用能力，兩種課程互相為用，又各自滿足不同年級的心理和實際需求。翻譯課程與商用課程互相補充；翻譯課程中的口譯課程又對會話課程加以補充；閱讀課程與大三、大四俄語互相補充，還可以透過閱讀知道俄國文化和文學相關知識，與文學史（以中俄文交雜上課）及俄羅斯概論（用中文授課）兩科互相補充。 </p>
