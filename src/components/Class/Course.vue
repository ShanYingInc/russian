    <p>◎ 應用俄語訓練</p>
    <p>三年級開設翻譯課程（俄文翻譯），為必修課程，著重筆譯的訓練；四年級開設口譯課程（實用俄語口譯、經貿俄語口譯），培養學生口譯能力，提供學生就業技能。商用俄語課程，培養學生商貿的口語和商業書信寫作能力，並鼓勵同學選修相關外系課程（例如：企管課程、外貿課程、電腦課程），培養學生將來就業的語言和專業知識能力。因應國際華語學習潮流及需求，本系也開授俄語華語教學課程，期能使學生開拓多元的就業選擇。</p>
