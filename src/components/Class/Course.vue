    <p>◎非語言部份：包含文化課程與文學課程</p>
    <p>本系為了讓學生更加了解俄國現況和社會文化，激發學習俄文的興趣，具備更寬廣的視野，於低年級（大一、大二）安排非語言課程，如「俄羅斯概論」、「俄羅斯民歌」、「俄國史」(一學年課程)及「俄羅斯文化概論」，使學生能初步了解俄國歷史、地理與文化概況，知道它的過去、現在及未來發展趨勢。三年級的「俄國文學史」，透過研讀文學史、閱讀俄語文學文本和課堂討論，進而誘發學生對文學的興趣，作為畢業後繼續研究的基礎，或是培養終生對文學的興趣，厚植豐厚的人文素養。</p>
    <h3>課程改革的精神：減少文化課程，增加語言應用課程和高階語言課程。</h3>
    <p>1.97學年度起廢除了「文化史」（由俄國現勢代替）、「俄羅斯民歌」（99學年度復開）、「俄國巡禮」等文化課程，將原先必修的「俄羅斯史」和「蘇聯史」改成選修，大幅度降低文化相關課程，將省下來的低年級學分轉往高年級，開設應用俄語課程和進階的語言相關課程，將「商用俄文（一）」從一個學期變成一年的選修課，與「商用俄文（二）」銜接，並與翻譯課程相結合（「經貿俄語口譯」）成為一個完整的商用俄文/商用翻譯課程。口譯課程的設置還可以增加同學口語會話的機會，弭補會話課時數不足的缺憾。 </p>
    <p>2.98學年度起停開「句法」與「詞法」，「句法」以「俄語短篇名著選讀」替代，「詞法」以「俄語小說與電影賞析」替代。</p>
